void HAL_SYSTICK_Callback(void)
{
	static uint8_t tim;
	for(int i=0;i<LEN(delay);i++)
	if(delay[i] >0) delay[i]--;	
	OS_TimeMS++;
	if(++tim ==10){	
        tim = 0;		
	    GUI_TOUCH_Exec();       
	}
	if(light >0) {
		light--;		
		if(!light) {
			lcdLight(0);
			HAL_GPIO_WritePin(GPIOC,GPIO_PIN_10,GPIO_PIN_SET);
		}
	}
} 
//----------------------------------------------------------------------
void onAlarm(void) 
{
	static uint8_t bit;
	uint8_t Error;	
	Error = 0;	
    for(int i=0;i<8;i++){
		temperature[i].alarm = (temperature[i].PV10 > temperature[i].SV10) ? 1 : 0;
		if(temperature[i].alarm) Error = 1; 
	}
    if(Error) HAL_GPIO_TogglePin(GPIOC,GPIO_PIN_10|GPIO_PIN_13); 
	if((bit)&&(!Error)) HAL_GPIO_WritePin(GPIOC,GPIO_PIN_10|GPIO_PIN_13,GPIO_PIN_RESET);
    bit = Error;		
	delay[alarm] = 400;//300ms
}


