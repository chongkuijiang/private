void LCD_CmdWrite(uint8_t Command)
{ 
	while(!LCD_RED_BUSY);
	LCD_CLR_CS1;
	LCD_SET_RS;   //高电平命令
	LCD_WRITE_DATA(Command);
	LCD_SET_RD;
	LCD_CLR_WR;
    delay1(1);
	LCD_SET_WR;
	LCD_SET_CS1;
	delay1(1);
}
