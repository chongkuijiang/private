// 功能描述:	向液晶屏发送字节
void LCD_DataWrite(u8 Data)
{	
 	while(!LCD_RED_BUSY);
	LCD_CLR_CS1;
	LCD_CLR_RS;   //数据	
	LCD_WRITE_DATA(Data);
    LCD_SET_RD;
    LCD_CLR_WR;
    delay1(1);
	LCD_SET_WR;	
	LCD_SET_CS1;
	delay1(1);
}
//---------------------------------------------------
u8 LCD_DataRead(void)
{
	u8 data;
	while(!LCD_RED_BUSY);
	LCD_CLR_CS1;
	LCD_CLR_RS;   //数据
	LCD_SET_DIR_IN();
	GPIOC->BSRR = LCD_DATA;          
    LCD_SET_WR;
	LCD_SET_RD;        
    delay1(1);
	LCD_CLR_RD; 
	data = (u8)LCD_RED_DATA;
	LCD_SET_DIR_OUT();
	LCD_SET_CS1;
	delay1(1);
	return data;
}
